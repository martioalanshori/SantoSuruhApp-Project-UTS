public abstract class Service {
    protected String name;
    protected double price;
    
    // Tambahkan constructor
    protected Service(String name, double price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }
}